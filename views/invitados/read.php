  <script >    
   $("input[type='submit']").click (function(){     
      buscar_code();
   });

   $("input[name='code']").change(function(e) {
        if (e.which == 13) {
              buscar_code();                            
            e.preventDefault();
        }
    });

   $(".back-to-top").click(function(){
      $("input[name='code']").val("");
   });

   $("input[type='button']").click(function(){
      $("input[name='code']").val("");
   });

   function buscar_code(){
    var code= $("input[name='code']").val();
      var parametro= {                  
          0: code                        
      };      
      //funcion Ajax  
        $.ajax({
            type: 'post',
            url: "?c=invitados&a=form_ajax",                        
            data: parametro,
        }).done(function(data) {
            var datos = data;            
            var obj= JSON.parse(datos);  
            if(obj.length > 0){
              $("#get_code span").text(obj[0]['code']);
              $("#get_nombres span").text(obj[0]['nombres']);
              $("#get_apellidos span").text(obj[0]['apellidos']);
              $("#get_silla span").text(obj[0]['numsilla']);
              $("#get_mesa span").text(obj[0]['nummesa']);
              $(document).scrollTop(900);
            }else{
              $("#get_code span").text("");
              $("#get_nombres span").text("");
              $("#get_apellidos span").text("");
              $("#get_silla span").text("");
              $("#get_mesa span").text("");
              $("input[name='code']").val("");
            }                           
        }).fail(function(data) {}).always( function(data) {
            //console.log(data);                
        });
   }

  </script>
